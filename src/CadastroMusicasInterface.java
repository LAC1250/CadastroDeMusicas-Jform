    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja excluir? ", "WARNING", JOptionPane.YES_NO_OPTION);
        if (opcao == 0) {
            Musica musica = musicas.get(i);
            Connection conn = FactoryConnection.getConnection();
            Musica_Dao dao = new Musica_Dao(conn);
            dao.delete(musica);
            musicas = dao.getAll();
            if (i != 0) {
                i--;
            }
            setTextField(musicas.get(i));
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(CadastroMusicasInterface.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

