    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
     Musica musica = new Musica(Integer.parseInt(tfID.getText()),tfTitulo.getText(),tfArtista.getText(),tfCompositor.getText(), new Genero("",1));
     
     Connection conn = FactoryConnection.getConnection();
     Musica_Dao dao = new Musica_Dao(conn);
        if(novo){
            int id = dao.save(musica);
            musica.setId(id);
        }else{
            dao.update(musica);
        }
        musicas = dao.getAll();
        i = musicas.indexOf(musica);
        setTextField(musicas.get(i));
        novo = false;
        editar = false;
        setStatusCampos(false);
        setStatusBotoes();   
    }//GEN-LAST:event_btnSalvarActionPerformed

